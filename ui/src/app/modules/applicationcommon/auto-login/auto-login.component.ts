import { Component, OnInit, OnDestroy } from '@angular/core';
import { BaseComponent } from 'src/app/base/component/base/base.component';
import { LoggerService } from 'src/app/base/service/logger.service';
import { OidcSecurityService } from 'angular-auth-oidc-client';
import { Subscription } from 'rxjs';

@Component({
    selector: 'app-fmp-auto-login',
    templateUrl: './auto-login.component.html',
    styleUrls: ['./auto-login.component.scss'],
})
export class AutoLoginComponent extends BaseComponent
    implements OnInit, OnDestroy {
    moduleSetupSubscription: Subscription;
    constructor(
        logger: LoggerService,
        private oidcSecurityService: OidcSecurityService) {
        super('AutoLoginComponent', logger);
        oidcSecurityService.onModuleSetup.subscribe(() => { this.onModuleSetup(); });
    }


    ngOnInit() {
        if (this.oidcSecurityService.moduleSetup) {
            this.onModuleSetup();
        }
    }
    private onModuleSetup() {
        this.oidcSecurityService.authorize();
    }
    ngOnDestroy(): void {
    }
}
